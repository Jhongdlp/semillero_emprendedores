// Helper function to get cantons by province
export function getCantonsByProvince(provinceName: string) {
  const province = ecuadorLocations.provinces.find(p => p.name === provinceName);
  return province?.cantons || [];
}

// Helper function to get parishes by canton
export function getParishesByCanton(provinceName: string, cantonName: string) {
  const province = ecuadorLocations.provinces.find(p => p.name === provinceName);
  const canton = province?.cantons.find(c => c.name === cantonName);
  return canton?.parishes || [];
}
